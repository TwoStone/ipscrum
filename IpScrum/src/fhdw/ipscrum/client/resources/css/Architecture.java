package fhdw.ipscrum.client.resources.css;

import com.google.gwt.resources.client.CssResource;

/**
 * Represents the css resource of the architecture.
 */
public interface Architecture extends CssResource {

	/**
	 * @return the height of the navigation.
	 */
	String NAVIGATION_HEIGHT();

	/**
	 * @return the css for the shadow color.
	 */
	String SHADOWCOLOR();

	/**
	 * @return the css of the breadcrumb.
	 */
	String breadcrumb();

	/**
	 * @return the css of the breadcrumb item.
	 */
	String breadcrumbItem();

}
